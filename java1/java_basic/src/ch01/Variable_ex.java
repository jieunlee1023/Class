package ch01;

public class Variable_ex {
	
	// 코드의 시작점
	public static void main(String[] args) {
		
		//변수! : 변하는 수
		// 변수를 선언
		int age; 
		int count;
		
		age = 1;
		count =10;
		
		System.out.println(age);
		System.out.println(count);
		
		age = 100;
		System.out.println(age);
		
		//변수선언과 동시에 초기화 하기
		int level = 2;
		level = 3;

	} //end of main

} //end of class
// 자바에서 블록의 범위는 절대적이다.
