package ch00;
//패키지(package) : 주파일들이 모여있는 폴더의 개념

public class Java {

	//main 코드의 시작점 (프로그래밍에 시작점)
	public static void main(String[] args) {
		
		System.out.println("Hello Java");
		//자바에서 명령어에 마지막은 세미콜론(;)으로 마무리한다.
		//System.out.println이란 ? 화면에 문자열을 라인 단위로 출력하는 자바 명령어이다.
		 
		// <--주석 (저장을 누르면 컴파일과정을 진행한다 , 컴파일 과정에서 무시)
		
		// 화면에 문자열을 출력 해보자.
		 System.out.println("안녕하세요~");
		 System.out.println();
		 System.out.println("반갑습니다~");
		 
	}//end of main

} //end of class

// {} 블록 (중괄호) : 자바에서 블록의 범위는 절대적입니다. 